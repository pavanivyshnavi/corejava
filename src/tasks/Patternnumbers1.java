package tasks;

public class Patternnumbers1 {

	public static void main(String[] args) {
		for(int row=1;row<=5;row++) {
			//for every row run the col
		for(int col=1;col<=5;col++) {
			System.out.print(col +" ");
		}
		//when one row is added print new line
		System.out.println();
		}

	}

}
